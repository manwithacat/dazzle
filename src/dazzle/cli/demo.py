DEFAULT_TEST_PASSWORD = "dazzle-test-2026"


def _generate_test_personas_toml(
    project_root: Path,
    blueprint_result: dict[str, Any],
) -> None:
    """Generate .dazzle/test_personas.toml from the demo blueprint.

    Does not overwrite if the file already exists.
    """

    toml_path = project_root / ".dazzle" / "test_personas.toml"
    if toml_path.exists():
        typer.echo(
            f"test_personas.toml already exists at {toml_path}, skipping credential generation."
        )
        return

    # Extract persona info from blueprint result
    personas_data: list[dict[str, Any]] = blueprint_result.get("personas", [])
    if not personas_data:
        return

    # Best-effort read of test_password override; default applies on any failure (#smells-1.1).
    test_password = DEFAULT_TEST_PASSWORD
    with suppress(Exception):
        import tomllib

        toml_conf = project_root / "dazzle.toml"
        if toml_conf.exists():
            with toml_conf.open("rb") as f:
                config = tomllib.load(f)
            test_password = config.get("demo", {}).get("test_password", DEFAULT_TEST_PASSWORD)

    lines = ["# Generated by 'dazzle demo propose' for journey testing", ""]
    for persona in personas_data:
        name = persona.get("persona_name", "").lower().replace(" ", "_")
        if not name:
            continue
        email = f"{name}@demo.example.com"
        lines.append(f"[personas.{name}]")
        lines.append(f'email = "{email}"')
        lines.append(f'password = "{test_password}"')
        lines.append("")

    if len(lines) > 2:  # More than just the header
        toml_path.parent.mkdir(parents=True, exist_ok=True)
        toml_path.write_text("\n".join(lines), encoding="utf-8")
        typer.echo(f"Generated test_personas.toml at {toml_path}")


def _find_data_dir(project_root: Path) -> Path | None:
    """Find the demo data directory.

    Order of precedence:
    1. ``demo_data/`` at the project root (explicit, user-curated)
    2. ``.dazzle/demo_data/`` (generated via ``dazzle demo generate``)
    3. ``dsl/seeds/demo_data/`` (blueprint-only — the default location
       used by ``dazzle init`` and by the example apps; #817)
    """
    for candidate in [
        project_root / "demo_data",
        project_root / ".dazzle" / "demo_data",
        project_root / "dsl" / "seeds" / "demo_data",
    ]:
        if candidate.is_dir():
            return candidate
    return None


@demo_app.command(name="load")
def load_command(
    base_url: str = typer.Option(
        "http://localhost:8000",
        "--base-url",
        "-u",
        help="Base URL of the running Dazzle instance",
    ),
    email: str = typer.Option(
        None,
        "--email",
        "-e",
        help="Admin email for authentication",
    ),
    password: str = typer.Option(
        None,
        "--password",
        "-p",
        help="Admin password for authentication",
    ),
    data_dir: Path | None = typer.Option(
        None,
        "--data-dir",
        "-d",
        help="Path to seed data directory (auto-detected if omitted)",
    ),
    entities: str | None = typer.Option(
        None,
        "--entities",
        help="Comma-separated entity names to load (default: all)",
    ),
    project_root: Path = typer.Option(
        Path("."),
        "--project",
        help="Project root directory",
    ),
) -> None:
    """Load demo seed data into a running Dazzle instance."""
    from dazzle.demo_data.loader import DemoDataLoader, topological_sort_entities

    project_root = project_root.resolve()

    # Parse DSL to get entity graph
    typer.echo("Parsing DSL...")
    try:
        from dazzle.cli.utils import load_project_appspec

        appspec = load_project_appspec(project_root)
    except Exception as e:
        typer.echo(f"Failed to parse DSL: {e}", err=True)
        raise typer.Exit(1)

    # Find data directory
    if data_dir is None:
        data_dir = _find_data_dir(project_root)
        if data_dir is None:
            typer.echo(
                "No demo data directory found. Run 'dazzle demo_data generate' first.", err=True
            )
            raise typer.Exit(1)
    typer.echo(f"Data directory: {data_dir}")

    # Topological sort
    entity_order = topological_sort_entities(appspec.domain.entities)
    typer.echo(f"Entity load order: {', '.join(entity_order)}")

    # Parse entity filter
    entities_filter = [e.strip() for e in entities.split(",")] if entities else None

    # Load
    with DemoDataLoader(base_url=base_url, email=email, password=password) as loader:
        if email and password:
            typer.echo("Authenticating...")
            try:
                loader.authenticate()
            except RuntimeError as e:
                typer.echo(f"Authentication failed: {e}", err=True)
                raise typer.Exit(1)

        typer.echo("Loading demo data...")
        report = loader.load_all(data_dir, entity_order, entities_filter=entities_filter)

    typer.echo("")
    typer.echo(report.summary())

    if report.total_failed > 0:
        raise typer.Exit(1)


@demo_app.command(name="validate")
def validate_command(
    data_dir: Path | None = typer.Option(
        None,
        "--data-dir",
        "-d",
        help="Path to seed data directory (auto-detected if omitted)",
    ),
    project_root: Path = typer.Option(
        Path("."),
        "--project",
        help="Project root directory",
    ),
) -> None:
    """Validate seed files against the DSL entity definitions."""
    from dazzle.demo_data.loader import validate_seed_data

    project_root = project_root.resolve()

    # Parse DSL
    typer.echo("Parsing DSL...")
    try:
        from dazzle.cli.utils import load_project_appspec

        appspec = load_project_appspec(project_root)
    except Exception as e:
        typer.echo(f"Failed to parse DSL: {e}", err=True)
        raise typer.Exit(1)

