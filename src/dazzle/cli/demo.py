@demo_app.command(name="reset-and-load")
def reset_and_load_command(
    project_root: Path = typer.Option(
        Path("."),
        "--project",
        help="Project root directory",
    ),
    base_url: str | None = typer.Option(
        None,
        "--base-url",
        "-u",
        help="API base URL (default: .dazzle/runtime.json api_url)",
    ),
    data_dir: Path | None = typer.Option(
        None,
        "--data-dir",
        "-d",
        help="Path to seed data directory (auto-detected if omitted)",
    ),
    yes: bool = typer.Option(
        False,
        "--yes",
        "-y",
        help="Skip confirmation prompt",
    ),
    json_output: bool = typer.Option(
        False,
        "--json",
        help="Emit full structured report as JSON",
    ),
    skip_verify: bool = typer.Option(
        False,
        "--skip-verify",
        help="Do not score persona_homes residual after seed",
    ),
) -> None:
    """Reset + seed via /__test__/* with STABLE persona ids (#1627).

    Prefer this over ``dazzle demo load`` for agent demos: it uses the
    same path as QA (no CSRF entity POSTs), reads ports/secret from
    runtime.json, and verifies assignment-aware persona homes.

    Order after this command: authenticate with role= (never before reset).
    """
    project_root = project_root.resolve()
    if not (project_root / "dazzle.toml").is_file():
        typer.echo(f"No dazzle.toml at {project_root}", err=True)
        raise typer.Exit(1)

    if not yes:
        confirm = typer.confirm(
            "This will POST /__test__/reset then seed jsonl fixtures. Continue?"
        )
        if not confirm:
            raise typer.Abort()

    report = reset_and_load(
        project_root,
        base_url=base_url,
        data_dir=data_dir,
        verify_persona_homes=not skip_verify,
    )

    if json_output:
        # Never dump raw test_secret in JSON stdout
        safe = {k: v for k, v in report.items() if k != "test_secret"}
        typer.echo(json.dumps(safe, indent=2, default=str))
    else:
        if report.get("ok"):
            typer.echo(
                f"OK reset-and-load: {report.get('fixture_count', 0)} fixtures → "
                f"{report.get('api_url')}"
            )
            residual = report.get("persona_homes_residual")
            if residual is not None:
                typer.echo(f"  persona_homes residual: {residual}")
            if report.get("warning"):
                typer.echo(f"  warning: {report['warning']}", err=True)
            typer.echo(f"  next: {report.get('next')}")
            typer.echo(
                f"  emails: {{role}}@{report.get('persona_email_domain', 'demo.dazzle.local')}"
            )
        else:
            typer.echo(f"FAIL: {report.get('error', 'unknown')}", err=True)
            playbook = report.get("playbook") or demo_ops_playbook()
            typer.echo("Tribal playbook:", err=True)
            for step in playbook.get("order", []):
                typer.echo(f"  · {step}", err=True)

    if not report.get("ok"):
        raise typer.Exit(1)


@demo_app.command(name="reset")
def reset_command(
    base_url: str = typer.Option(
        "http://localhost:8000",
        "--base-url",
        "-u",
        help="Base URL of the running Dazzle instance",
    ),
    email: str = typer.Option(
        None,
        "--email",
        "-e",
        help="Admin email for authentication",
    ),
    password: str = typer.Option(
        None,
        "--password",
        "-p",
        help="Admin password for authentication",
    ),
    data_dir: Path | None = typer.Option(
        None,
        "--data-dir",
        "-d",
        help="Path to seed data directory (auto-detected if omitted)",
    ),
    project_root: Path = typer.Option(
        Path("."),
        "--project",
        help="Project root directory",
    ),
    yes: bool = typer.Option(
        False,
        "--yes",
        "-y",
        help="Skip confirmation prompt",
    ),
) -> None:
    """Clear all entity data and reload from seed files.

    WARNING: This deletes all existing data in reverse dependency order,
    then reloads from seed files.
    """
    from dazzle.demo_data.loader import DemoDataLoader, topological_sort_entities

    project_root = project_root.resolve()

    if not yes:
        confirm = typer.confirm("This will DELETE all entity data and reload. Continue?")
        if not confirm:
            raise typer.Abort()

    # Parse DSL
    typer.echo("Parsing DSL...")
    try:
        from dazzle.cli.utils import load_project_appspec

        appspec = load_project_appspec(project_root)
    except Exception as e:
        typer.echo(f"Failed to parse DSL: {e}", err=True)
        raise typer.Exit(1)

    # Find data directory
    if data_dir is None:
        data_dir = _find_data_dir(project_root)
        if data_dir is None:
            typer.echo("No demo data directory found.", err=True)
            raise typer.Exit(1)

    entity_order = topological_sort_entities(appspec.domain.entities)

    with DemoDataLoader(base_url=base_url, email=email, password=password) as loader:
        if email and password:
            typer.echo("Authenticating...")
            try:
                loader.authenticate()
            except RuntimeError as e:
                typer.echo(f"Authentication failed: {e}", err=True)
                raise typer.Exit(1)

        # Delete in reverse order (children first)
        client = loader._get_client()
        typer.echo("Deleting existing data (reverse dependency order)...")
        for entity_name in reversed(entity_order):
            from dazzle.core.strings import to_api_plural

            endpoint = f"/{to_api_plural(entity_name)}"
            try:
                resp = client.delete(endpoint, headers=loader._headers())
                if resp.status_code in (200, 204):
                    typer.echo(f"  Cleared {entity_name}")
                elif resp.status_code == 404:
                    typer.echo(f"  {entity_name}: no delete endpoint")
                else:
                    typer.echo(f"  {entity_name}: HTTP {resp.status_code}")
            except Exception as e:
                typer.echo(f"  {entity_name}: {e}")

        # Reload
        typer.echo("\nReloading demo data...")
        report = loader.load_all(data_dir, entity_order)

    typer.echo("")
    typer.echo(report.summary())

    if report.total_failed > 0:
        raise typer.Exit(1)


