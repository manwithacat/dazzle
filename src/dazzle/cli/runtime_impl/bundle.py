"""Production-bundle file generators for ``dazzle build``.

Emits the framework-agnostic deployment artifacts (entry point, requirements,
env template) that make up a buildpack-ready bundle — no containerisation.
Developers who want Docker / k8s roll their own against these files.
"""


def generate_production_main(app_name: str, include_frontend: bool) -> str:
    """Generate production-ready main.py entry point."""
    return f'''"""
Production entry point for {app_name}.

Auto-generated by Dazzle Dazzle build.

Usage:
    python main.py                    # Run with defaults
    python main.py --port 8080        # Custom port
    python main.py --host 0.0.0.0     # Bind to all interfaces

Environment variables:
    PORT          - Server port (default: 8000)
    HOST          - Server host (default: 0.0.0.0)
    DATABASE_URL  - PostgreSQL connection URL
    REDIS_URL     - Redis connection URL
    LOG_LEVEL     - Logging level (default: INFO)
"""

import argparse
import logging
import os
from pathlib import Path

# Configure logging
log_level = os.getenv("LOG_LEVEL", "INFO").upper()
logging.basicConfig(
    level=getattr(logging, log_level, logging.INFO),
    format="%(asctime)s - %(name)s - %(levelname)s - %(message)s"
)
logger = logging.getLogger(__name__)


def main():
    parser = argparse.ArgumentParser(description="{app_name} - Dazzle Production Server")
    parser.add_argument("--port", type=int, default=int(os.getenv("PORT", "8000")))
    parser.add_argument("--host", type=str, default=os.getenv("HOST", "0.0.0.0"))
    parser.add_argument("--reload", action="store_true", help="Enable auto-reload (dev only)")
    args = parser.parse_args()

    # Database setup
    db_url = os.getenv("DATABASE_URL", "")
    if not db_url:
        logger.error("DATABASE_URL environment variable is required")
        return 1

    logger.info(f"Starting {app_name}")
    logger.info(f"  Host: {{args.host}}:{{args.port}}")
    logger.info(f"  Database: {{db_url}}")

    try:
        import uvicorn
        from dazzle.http.runtime import create_app_from_json

        spec_path = Path(__file__).parent / "backend" / "backend-spec.json"
        if not spec_path.exists():
            logger.error(f"Backend spec not found: {{spec_path}}")
            return 1

        app = create_app_from_json(str(spec_path), db_path=db_url)

        uvicorn.run(
            app,
            host=args.host,
            port=args.port,
            reload=args.reload,
            log_level=log_level.lower(),
        )

    except ImportError as e:
        logger.error(f"Missing dependencies: {{e}}")
        logger.error("Install with: pip install -r requirements.txt")
        return 1
    except Exception as e:
        logger.error(f"Failed to start: {{e}}")
        return 1

    return 0


if __name__ == "__main__":
    exit(main())
'''


