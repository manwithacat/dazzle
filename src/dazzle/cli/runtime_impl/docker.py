def generate_dev_compose(
    project_name: str,
    pg_port: int = 5432,
    redis_port: int = 6379,
) -> str:
    """Generate docker-compose.yml for dev infrastructure (Postgres + Redis).

    This compose file provides backing services only. The Dazzle app
    runs on the host and connects to these services.

    Args:
        project_name: Project name (used in comments).
        pg_port: Host port for PostgreSQL (default 5432).
        redis_port: Host port for Redis (default 6379).

    Returns:
        docker-compose.yml content string.
    """
    return DEV_COMPOSE_TEMPLATE.format(
        project_name=project_name,
        pg_port=pg_port,
        redis_port=redis_port,
    )


def dev_compose_path(project_root: Path) -> Path:
    """Return the path to the dev infrastructure compose file."""
    return project_root / ".dazzle" / "docker-compose.yml"


def write_dev_compose(
    project_root: Path,
    project_name: str,
    pg_port: int = 5432,
    redis_port: int = 6379,
) -> Path:
    """Write the dev infrastructure compose file to .dazzle/.

    Args:
        project_root: Project root directory.
        project_name: Project name.
        pg_port: Host port for PostgreSQL.
        redis_port: Host port for Redis.

    Returns:
        Path to the written compose file.
    """
    compose_file = dev_compose_path(project_root)
    compose_file.parent.mkdir(parents=True, exist_ok=True)
    compose_file.write_text(
        generate_dev_compose(project_name, pg_port=pg_port, redis_port=redis_port)
    )
    return compose_file


def dev_database_url(pg_port: int = 5432) -> str:
    """Return the DATABASE_URL for the dev infrastructure Postgres."""
    return f"postgresql://dazzle:dazzle@localhost:{pg_port}/dazzle"


def dev_redis_url(redis_port: int = 6379) -> str:
    """Return the REDIS_URL for the dev infrastructure Redis."""
    return f"redis://localhost:{redis_port}/0"


def start_dev_infrastructure(
    project_root: Path,
    project_name: str,
    pg_port: int = 5432,
    redis_port: int = 6379,
    timeout: int = 60,
) -> tuple[str, str]:
    """Start dev infrastructure (Postgres + Redis) via Docker Compose.

    Writes the compose file, runs ``docker compose up -d``, and waits
    for healthchecks to pass.

    Args:
        project_root: Project root directory.
        project_name: Project name.
        pg_port: Host port for PostgreSQL.
        redis_port: Host port for Redis.
        timeout: Max seconds to wait for services to become healthy.

    Returns:
        (database_url, redis_url) tuple.

    Raises:
        RuntimeError: If Docker is not available or services fail to start.
    """
    compose_file = write_dev_compose(
        project_root, project_name, pg_port=pg_port, redis_port=redis_port
    )

    # Start services
    try:
        result = subprocess.run(
            ["docker", "compose", "-f", str(compose_file), "up", "-d"],
            capture_output=True,
            text=True,
            timeout=120,
        )
    except FileNotFoundError:
        raise RuntimeError("Docker is not installed. Install Docker or use: dazzle serve --local")
    except subprocess.TimeoutExpired:
        raise RuntimeError("Docker compose up timed out")

    if result.returncode != 0:
        raise RuntimeError(f"Docker compose up failed:\n{result.stderr}")

    # Wait for services to be healthy
    deadline = time.monotonic() + timeout
    while time.monotonic() < deadline:
        try:
            health = subprocess.run(
                [
                    "docker",
                    "compose",
                    "-f",
                    str(compose_file),
                    "ps",
                    "--format",
                    "{{.Health}}",
                ],
                capture_output=True,
                text=True,
                timeout=10,
            )
            lines = [ln.strip() for ln in health.stdout.strip().splitlines() if ln.strip()]
            if lines and all(ln == "healthy" for ln in lines):
                break
        except (subprocess.SubprocessError, subprocess.TimeoutExpired):
            pass
        time.sleep(2)
    else:
        raise RuntimeError(
            f"Dev infrastructure services did not become healthy within {timeout}s. "
            f"Check: docker compose -f {compose_file} ps"
        )

    return dev_database_url(pg_port), dev_redis_url(redis_port)


def stop_dev_infrastructure(project_root: Path) -> bool:
    """Stop dev infrastructure if running.

    Args:
        project_root: Project root directory.

    Returns:
        True if stopped successfully (or compose file didn't exist).
    """
    compose_file = dev_compose_path(project_root)
    if not compose_file.exists():
        return True

    try:
        result = subprocess.run(
            ["docker", "compose", "-f", str(compose_file), "down"],
            capture_output=True,
            text=True,
            timeout=60,
        )
        return result.returncode == 0
    except (subprocess.SubprocessError, FileNotFoundError):
        return False
