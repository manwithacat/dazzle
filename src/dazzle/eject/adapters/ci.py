"""
CI adapters for ejection.

Generates CI/CD configuration files:
- GitHub Actions workflows
- GitLab CI pipelines
- Build and deployment scripts
"""

from __future__ import annotations

from typing import TYPE_CHECKING

from dazzle.eject.generator import GeneratorResult

from .base import AdapterRegistry, CIAdapter

if TYPE_CHECKING:
    pass


class GitHubActionsAdapter(CIAdapter):
    """
    Generates GitHub Actions workflows.

    Creates workflows for:
    - CI (lint, type check, test)
    - Contract testing
    - Docker build
    - Deployment
    """

    def generate(self) -> GeneratorResult:
        """Generate GitHub Actions workflows."""
        result = GeneratorResult()

        result.merge(self._generate_ci_workflow())
        result.merge(self._generate_contract_workflow())
        result.merge(self._generate_docker_workflow())
        result.merge(self._generate_deploy_workflow())
        result.merge(self._generate_dependabot())

        return result

    def _generate_ci_workflow(self) -> GeneratorResult:
        """Generate main CI workflow."""
        result = GeneratorResult()

        _app_name = self.spec.name.lower().replace(" ", "-")  # Reserved for future use

        content = f"""# CI Workflow for {self.spec.name}
# Generated by DAZZLE Ejection Toolchain v0.7.2

name: CI

on:
  push:
    branches: [main, develop]
  pull_request:
    branches: [main]

env:
  PYTHON_VERSION: "3.11"
  NODE_VERSION: "20"

jobs:
  lint:
    name: Lint
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}

      - name: Install dependencies
        run: |
          python -m pip install --upgrade pip
          pip install ruff mypy

      - name: Run Ruff
        run: ruff check backend/

      - name: Run Ruff Format Check
        run: ruff format --check backend/

  typecheck:
    name: Type Check
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}

      - name: Install dependencies
        run: |
          python -m pip install --upgrade pip
          pip install -r backend/requirements.txt
          pip install mypy

      - name: Run mypy
        run: mypy backend/app/

  test:
    name: Test
    runs-on: ubuntu-latest
    needs: [lint, typecheck]
    services:
      postgres:
        image: postgres:15
        env:
          POSTGRES_USER: test
          POSTGRES_PASSWORD: test
          POSTGRES_DB: test
        ports:
          - 5432:5432
        options: >-
          --health-cmd pg_isready
          --health-interval 10s
          --health-timeout 5s
          --health-retries 5

    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{{{ env.PYTHON_VERSION }}}}

      - name: Install dependencies
        run: |
          python -m pip install --upgrade pip
          pip install -r backend/requirements.txt
          pip install pytest pytest-cov pytest-asyncio

      - name: Run tests
        env:
          DATABASE_URL: postgresql://test:test@localhost:5432/test
        run: |
          pytest tests/unit/ -v --cov=app --cov-report=xml

      - name: Upload coverage
        uses: codecov/codecov-action@v4
        with:
          files: ./coverage.xml
          fail_ci_if_error: false

  frontend-lint:
    name: Frontend Lint
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Set up Node.js
        uses: actions/setup-node@v4
        with:
          node-version: ${{{{ env.NODE_VERSION }}}}
          cache: "npm"
          cache-dependency-path: frontend/package-lock.json

      - name: Install dependencies
        working-directory: frontend
        run: npm ci

      - name: Run ESLint
        working-directory: frontend
        run: npm run lint

      - name: Run TypeScript check
        working-directory: frontend
        run: npm run typecheck

  all-checks:
    name: All Checks
    runs-on: ubuntu-latest
    needs: [lint, typecheck, test, frontend-lint]
    steps:
      - name: All checks passed
        run: echo "All CI checks passed!"
"""

        result.add_file(
            self.output_dir / ".github" / "workflows" / "ci.yml",
            content,
        )
        return result

    def _generate_contract_workflow(self) -> GeneratorResult:
        """Generate contract testing workflow."""
        result = GeneratorResult()

        content = """# Contract Testing Workflow
# Generated by DAZZLE Ejection Toolchain v0.7.2

name: Contract Tests

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]
  schedule:
    # Run daily at midnight
    - cron: "0 0 * * *"

env:
  PYTHON_VERSION: "3.11"

jobs:
  contract-tests:
    name: Schemathesis Contract Tests
    runs-on: ubuntu-latest
    services:
      postgres:
        image: postgres:15
        env:
          POSTGRES_USER: test
          POSTGRES_PASSWORD: test
          POSTGRES_DB: test
        ports:
          - 5432:5432
        options: >-
          --health-cmd pg_isready
          --health-interval 10s
          --health-timeout 5s
          --health-retries 5

    steps:
      - uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: ${{ env.PYTHON_VERSION }}

      - name: Install dependencies
        run: |
          python -m pip install --upgrade pip
          pip install -r backend/requirements.txt
          pip install schemathesis pytest

      - name: Start API server
        env:
          DATABASE_URL: postgresql://test:test@localhost:5432/test
        run: |
          cd backend
          uvicorn app.main:app --host 0.0.0.0 --port 8000 &
          sleep 5

      - name: Run Schemathesis tests
        run: |
          schemathesis run http://localhost:8000/openapi.json \\
            --checks all \\
            --base-url http://localhost:8000 \\
            --hypothesis-max-examples=100

      - name: Run contract test suite
        env:
          DATABASE_URL: postgresql://test:test@localhost:5432/test
        run: pytest tests/contract/ -v
"""

        result.add_file(
            self.output_dir / ".github" / "workflows" / "contract.yml",
            content,
        )
        return result

    def _generate_docker_workflow(self) -> GeneratorResult:
        """Generate Docker build workflow."""
        result = GeneratorResult()

        _app_name = self.spec.name.lower().replace(" ", "-")  # Reserved for future use

        content = """# Docker Build Workflow
# Generated by DAZZLE Ejection Toolchain v0.7.2

name: Docker Build

on:
  push:
    branches: [main]
    tags: ["v*"]
  pull_request:
    branches: [main]

env:
  REGISTRY: ghcr.io
  IMAGE_NAME: ${{ github.repository }}

jobs:
  build-backend:
    name: Build Backend
    runs-on: ubuntu-latest
    permissions:
      contents: read
      packages: write

    steps:
      - uses: actions/checkout@v4

      - name: Set up Docker Buildx
        uses: docker/setup-buildx-action@v3

      - name: Log in to Container Registry
        if: github.event_name != 'pull_request'
        uses: docker/login-action@v3
        with:
          registry: ${{ env.REGISTRY }}
          username: ${{ github.actor }}
          password: ${{ secrets.GITHUB_TOKEN }}

      - name: Extract metadata
        id: meta
        uses: docker/metadata-action@v5
        with:
          images: ${{ env.REGISTRY }}/${{ env.IMAGE_NAME }}/backend
          tags: |
            type=ref,event=branch
            type=ref,event=pr
            type=semver,pattern={{version}}
            type=sha

      - name: Build and push
        uses: docker/build-push-action@v5
        with:
          context: ./backend
          push: ${{ github.event_name != 'pull_request' }}
          tags: ${{ steps.meta.outputs.tags }}
          labels: ${{ steps.meta.outputs.labels }}
          cache-from: type=gha
          cache-to: type=gha,mode=max

  build-frontend:
    name: Build Frontend
    runs-on: ubuntu-latest
    permissions:
      contents: read
      packages: write

    steps:
      - uses: actions/checkout@v4

      - name: Set up Docker Buildx
        uses: docker/setup-buildx-action@v3

      - name: Log in to Container Registry
        if: github.event_name != 'pull_request'
        uses: docker/login-action@v3
        with:
          registry: ${{ env.REGISTRY }}
          username: ${{ github.actor }}
          password: ${{ secrets.GITHUB_TOKEN }}

      - name: Extract metadata
        id: meta
        uses: docker/metadata-action@v5
        with:
          images: ${{ env.REGISTRY }}/${{ env.IMAGE_NAME }}/frontend
          tags: |
            type=ref,event=branch
            type=ref,event=pr
            type=semver,pattern={{version}}
            type=sha

      - name: Build and push
        uses: docker/build-push-action@v5
        with:
          context: ./frontend
          push: ${{ github.event_name != 'pull_request' }}
          tags: ${{ steps.meta.outputs.tags }}
          labels: ${{ steps.meta.outputs.labels }}
          cache-from: type=gha
          cache-to: type=gha,mode=max
          build-args: |
            VITE_API_URL=/api
"""

        result.add_file(
            self.output_dir / ".github" / "workflows" / "docker.yml",
            content,
        )
        return result

    def _generate_deploy_workflow(self) -> GeneratorResult:
        """Generate deployment workflow."""
        result = GeneratorResult()

        content = """# Deployment Workflow
# Generated by DAZZLE Ejection Toolchain v0.7.2

name: Deploy

on:
  push:
    tags: ["v*"]
  workflow_dispatch:
    inputs:
      environment:
        description: "Deployment environment"
        required: true
        default: "staging"
        type: choice
        options:
          - staging
          - production

env:
  REGISTRY: ghcr.io
  IMAGE_NAME: ${{ github.repository }}

jobs:
  deploy-staging:
    name: Deploy to Staging
    runs-on: ubuntu-latest
    if: github.event.inputs.environment == 'staging' || (github.event_name == 'push' && contains(github.ref, 'rc'))
    environment: staging

    steps:
      - uses: actions/checkout@v4

      - name: Deploy to staging
        run: |
          echo "Deploying to staging environment..."
          # Add your deployment commands here
          # Examples:
          # - kubectl apply -f k8s/staging/
          # - aws ecs update-service ...
          # - fly deploy --config fly.staging.toml

  deploy-production:
    name: Deploy to Production
    runs-on: ubuntu-latest
    if: github.event.inputs.environment == 'production' || (github.event_name == 'push' && !contains(github.ref, 'rc'))
    environment: production

    steps:
      - uses: actions/checkout@v4

      - name: Deploy to production
        run: |
          echo "Deploying to production environment..."
          # Add your deployment commands here
          # Examples:
          # - kubectl apply -f k8s/production/
          # - aws ecs update-service ...
          # - fly deploy --config fly.production.toml

      - name: Notify deployment
        if: success()
        run: |
          echo "Deployment to production successful!"
          # Add notification commands (Slack, Discord, etc.)
"""

        result.add_file(
            self.output_dir / ".github" / "workflows" / "deploy.yml",
            content,
        )
        return result

    def _generate_dependabot(self) -> GeneratorResult:
        """Generate Dependabot configuration."""
        result = GeneratorResult()

        content = """# Dependabot Configuration
# Generated by DAZZLE Ejection Toolchain v0.7.2

version: 2
updates:
  # Python dependencies
  - package-ecosystem: "pip"
    directory: "/backend"
    schedule:
      interval: "weekly"
    open-pull-requests-limit: 5
    labels:
      - "dependencies"
      - "python"
    commit-message:
      prefix: "chore(deps)"

  # NPM dependencies
  - package-ecosystem: "npm"
    directory: "/frontend"
    schedule:
      interval: "weekly"
    open-pull-requests-limit: 5
    labels:
      - "dependencies"
      - "javascript"
    commit-message:
      prefix: "chore(deps)"

  # GitHub Actions
  - package-ecosystem: "github-actions"
    directory: "/"
    schedule:
      interval: "weekly"
    open-pull-requests-limit: 5
    labels:
      - "dependencies"
      - "github-actions"
    commit-message:
      prefix: "chore(deps)"

  # Docker
  - package-ecosystem: "docker"
    directory: "/backend"
    schedule:
      interval: "weekly"
    labels:
      - "dependencies"
      - "docker"

  - package-ecosystem: "docker"
    directory: "/frontend"
    schedule:
      interval: "weekly"
    labels:
      - "dependencies"
      - "docker"
"""

        result.add_file(
            self.output_dir / ".github" / "dependabot.yml",
            content,
        )
        return result


class GitLabCIAdapter(CIAdapter):
    """
    Generates GitLab CI pipelines.

    Creates .gitlab-ci.yml with stages for:
    - Lint and type check
    - Test
    - Build
    - Deploy
    """

    def generate(self) -> GeneratorResult:
        """Generate GitLab CI configuration."""
        result = GeneratorResult()

        content = """# GitLab CI Pipeline
# Generated by DAZZLE Ejection Toolchain v0.7.2

stages:
  - lint
  - test
  - build
  - deploy

variables:
  PYTHON_VERSION: "3.11"
  NODE_VERSION: "20"
  PIP_CACHE_DIR: "$CI_PROJECT_DIR/.cache/pip"
  NPM_CONFIG_CACHE: "$CI_PROJECT_DIR/.cache/npm"

cache:
  key: "${CI_COMMIT_REF_SLUG}"
  paths:
    - .cache/pip
    - .cache/npm
    - frontend/node_modules

# Lint Stage
lint:python:
  stage: lint
  image: python:${PYTHON_VERSION}
  script:
    - pip install ruff
    - ruff check backend/
    - ruff format --check backend/

lint:frontend:
  stage: lint
  image: node:${NODE_VERSION}
  script:
    - cd frontend
    - npm ci
    - npm run lint
    - npm run typecheck

typecheck:python:
  stage: lint
  image: python:${PYTHON_VERSION}
  script:
    - pip install -r backend/requirements.txt
    - pip install mypy
    - mypy backend/app/

# Test Stage
test:unit:
  stage: test
  image: python:${PYTHON_VERSION}
  services:
    - postgres:15
  variables:
    POSTGRES_DB: test
    POSTGRES_USER: test
    POSTGRES_PASSWORD: test
    DATABASE_URL: postgresql://test:test@postgres:5432/test
  script:
    - pip install -r backend/requirements.txt
    - pip install pytest pytest-cov pytest-asyncio
    - pytest tests/unit/ -v --cov=app --cov-report=xml
  artifacts:
    reports:
      coverage_report:
        coverage_format: cobertura
        path: coverage.xml

test:contract:
  stage: test
  image: python:${PYTHON_VERSION}
  services:
    - postgres:15
  variables:
    POSTGRES_DB: test
    POSTGRES_USER: test
    POSTGRES_PASSWORD: test
    DATABASE_URL: postgresql://test:test@postgres:5432/test
  script:
    - pip install -r backend/requirements.txt
    - pip install schemathesis pytest
    - cd backend && uvicorn app.main:app --host 0.0.0.0 --port 8000 &
    - sleep 5
    - schemathesis run http://localhost:8000/openapi.json --checks all
    - pytest tests/contract/ -v

# Build Stage
build:backend:
  stage: build
  image: docker:24
  services:
    - docker:24-dind
  script:
    - docker build -t $CI_REGISTRY_IMAGE/backend:$CI_COMMIT_SHA ./backend
    - docker push $CI_REGISTRY_IMAGE/backend:$CI_COMMIT_SHA
  rules:
    - if: $CI_COMMIT_BRANCH == "main"
    - if: $CI_COMMIT_TAG

build:frontend:
  stage: build
  image: docker:24
  services:
    - docker:24-dind
  script:
    - docker build -t $CI_REGISTRY_IMAGE/frontend:$CI_COMMIT_SHA ./frontend
    - docker push $CI_REGISTRY_IMAGE/frontend:$CI_COMMIT_SHA
  rules:
    - if: $CI_COMMIT_BRANCH == "main"
    - if: $CI_COMMIT_TAG

# Deploy Stage
deploy:staging:
  stage: deploy
  image: alpine:latest
  environment:
    name: staging
    url: https://staging.example.com
  script:
    - echo "Deploying to staging..."
    # Add deployment commands
  rules:
    - if: $CI_COMMIT_BRANCH == "main"
  when: manual

deploy:production:
  stage: deploy
  image: alpine:latest
  environment:
    name: production
    url: https://example.com
  script:
    - echo "Deploying to production..."
    # Add deployment commands
  rules:
    - if: $CI_COMMIT_TAG
  when: manual
"""

        result.add_file(
            self.output_dir / ".gitlab-ci.yml",
            content,
        )
        return result


# Register adapters
AdapterRegistry.register_ci("github-actions", GitHubActionsAdapter)
AdapterRegistry.register_ci("gitlab-ci", GitLabCIAdapter)
