"""
Middleware generator for Next.js Onebox.

Generates:
- src/middleware.ts (Next.js middleware for auth and persona)
"""

from pathlib import Path

from ....core import ir
from ...base.generator import Generator, GeneratorResult


class MiddlewareGenerator(Generator):
    """Generates Next.js middleware."""

    def generate(self) -> GeneratorResult:
        """Generate middleware file."""
        result = GeneratorResult()

        self._generate_middleware(result)

        return result

    def _generate_middleware(self, result: GeneratorResult) -> None:
        """Generate Next.js middleware for auth."""
        content = '''// Middleware
// Generated by DAZZLE Next.js Onebox Stack

import { NextResponse } from "next/server";
import type { NextRequest } from "next/server";
import { jwtVerify } from "jose";

const JWT_SECRET = new TextEncoder().encode(
  process.env.JWT_SECRET || "your-secret-key-change-in-production"
);

// Routes that don't require authentication
const publicRoutes = [
  "/login",
  "/register",
  "/api/health",
];

// Routes that should redirect to home if authenticated
const authRoutes = [
  "/login",
  "/register",
];

export async function middleware(request: NextRequest) {
  const { pathname } = request.nextUrl;

  // Allow public routes
  if (publicRoutes.some((route) => pathname.startsWith(route))) {
    // If already authenticated and trying to access auth routes, redirect to home
    if (authRoutes.some((route) => pathname.startsWith(route))) {
      const isAuthenticated = await checkAuth(request);
      if (isAuthenticated) {
        return NextResponse.redirect(new URL("/", request.url));
      }
    }
    return NextResponse.next();
  }

  // Check authentication for protected routes
  const isAuthenticated = await checkAuth(request);

  if (!isAuthenticated) {
    // Redirect to login
    const loginUrl = new URL("/login", request.url);
    loginUrl.searchParams.set("from", pathname);
    return NextResponse.redirect(loginUrl);
  }

  return NextResponse.next();
}

async function checkAuth(request: NextRequest): Promise<boolean> {
  const token = request.cookies.get("session")?.value;

  if (!token) {
    return false;
  }

  try {
    await jwtVerify(token, JWT_SECRET);
    return true;
  } catch {
    return false;
  }
}

export const config = {
  matcher: [
    /*
     * Match all request paths except:
     * - _next/static (static files)
     * - _next/image (image optimization files)
     * - favicon.ico (favicon file)
     * - public files (public folder)
     */
    "/((?!_next/static|_next/image|favicon.ico|.*\\\\.(?:svg|png|jpg|jpeg|gif|webp)$).*)",
  ],
};
'''
        path = self.output_dir / "src" / "middleware.ts"
        self._write_file(path, content)
        result.add_file(path)
